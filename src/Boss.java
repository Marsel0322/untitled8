public class Boss {
    private int Health;
    private int Damage;
    private String Deffens;

    public int getHealth() {
        return Health;
    }

    public void setHealth(int health) {
        Health = health;
    }

    public int getDamage() {
        return Damage;
    }

    public void setDamage(int damage) {
        Damage = damage;
    }

    public String getDeffens() {
        return Deffens;
    }

    public void setDeffens(String deffens) {
        Deffens = deffens;
    }
}
